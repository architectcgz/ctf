#!/usr/bin/env python3
from __future__ import annotations

import argparse
import sys
from pathlib import Path

try:
    import yaml
except Exception as exc:  # pragma: no cover - runtime dependency check
    raise SystemExit(f"缺少 PyYAML，无法同步 OpenAPI 契约: {exc}")


REPO_ROOT = Path(__file__).resolve().parents[1]
CONTRACTS_DIR = REPO_ROOT / "docs" / "contracts"
SOURCE_ROOT = CONTRACTS_DIR / "openapi-v1"
SOURCE_INDEX = SOURCE_ROOT / "index.yaml"
BUNDLE_PATH = CONTRACTS_DIR / "openapi-v1.yaml"
BUNDLE_HEADER = """# Generated from docs/contracts/openapi-v1/index.yaml by scripts/sync_openapi_from_contract.py.
# Do not edit this file directly; edit the split source under docs/contracts/openapi-v1/ and rerun the sync script.
"""


class CompactDumper(yaml.SafeDumper):
    pass


def _is_scalar(value: object) -> bool:
    return not isinstance(value, (dict, list, tuple))


def _should_use_flow_mapping(data: dict[object, object]) -> bool:
    if not data or len(data) > 3:
        return False
    for value in data.values():
        if isinstance(value, dict):
            if len(value) > 2:
                return False
            if any(not _is_scalar(nested) for nested in value.values()):
                return False
            continue
        if isinstance(value, list):
            if len(value) > 6 or any(not _is_scalar(item) for item in value):
                return False
            continue
        if not _is_scalar(value):
            return False
    return True


def _should_use_flow_sequence(data: list[object]) -> bool:
    return 0 < len(data) <= 6 and all(_is_scalar(item) for item in data)


def _represent_dict(dumper: CompactDumper, data: dict[object, object]) -> yaml.nodes.MappingNode:
    return dumper.represent_mapping("tag:yaml.org,2002:map", data.items(), flow_style=_should_use_flow_mapping(data))


def _represent_list(dumper: CompactDumper, data: list[object]) -> yaml.nodes.SequenceNode:
    return dumper.represent_sequence("tag:yaml.org,2002:seq", data, flow_style=_should_use_flow_sequence(data))


CompactDumper.add_representer(dict, _represent_dict)
CompactDumper.add_representer(list, _represent_list)
CompactDumper.ignore_aliases = lambda self, data: True


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="从拆分源同步 OpenAPI bundle")
    parser.add_argument(
        "--source",
        default=str(SOURCE_INDEX),
        help="拆分源入口文件，默认 docs/contracts/openapi-v1/index.yaml",
    )
    parser.add_argument(
        "--output",
        default=str(BUNDLE_PATH),
        help="bundle 输出路径，默认 docs/contracts/openapi-v1.yaml",
    )
    parser.add_argument("--check", action="store_true", help="仅检查 bundle 是否与拆分源一致")
    return parser.parse_args()


def fail(message: str) -> "Never":
    raise SystemExit(f"错误: {message}")


def load_yaml(path: Path) -> object:
    if not path.is_file():
        fail(f"缺少 YAML 文件: {path}")
    try:
        payload = yaml.safe_load(path.read_text(encoding="utf-8"))
    except yaml.YAMLError as exc:
        fail(f"解析 YAML 失败 {path}: {exc}")
    return {} if payload is None else payload


def merge_unique(target: dict[object, object], incoming: dict[object, object], source: Path) -> dict[object, object]:
    for key, value in incoming.items():
        if key in target:
            fail(f"重复 key: {key!r} 来自 {source}")
        target[key] = value
    return target


def resolve_node(node: object, current_file: Path) -> object:
    if isinstance(node, dict):
        if "$merge" in node:
            if set(node.keys()) != {"$merge"}:
                fail(f"{current_file} 中 $merge 不能与其他字段并存")
            merge_items = node["$merge"]
            if not isinstance(merge_items, list):
                fail(f"{current_file} 中 $merge 必须是列表")
            merged: dict[object, object] = {}
            for item in merge_items:
                if not isinstance(item, str):
                    fail(f"{current_file} 中 $merge 项必须是相对路径字符串")
                fragment_path = (current_file.parent / item).resolve()
                fragment = load_yaml(fragment_path)
                resolved_fragment = resolve_node(fragment, fragment_path)
                if not isinstance(resolved_fragment, dict):
                    fail(f"{fragment_path} 必须解析为对象，当前是 {type(resolved_fragment).__name__}")
                merge_unique(merged, resolved_fragment, fragment_path)
            return merged
        return {key: resolve_node(value, current_file) for key, value in node.items()}
    if isinstance(node, list):
        return [resolve_node(item, current_file) for item in node]
    return node


def load_bundle(source_path: Path) -> dict[object, object]:
    source = load_yaml(source_path)
    if not isinstance(source, dict):
        fail(f"{source_path} 顶层必须是对象")
    bundle = resolve_node(source, source_path)
    if not isinstance(bundle, dict):
        fail(f"{source_path} 解析结果必须是对象")
    required_keys = ["openapi", "info", "servers", "tags", "components", "paths"]
    missing = [key for key in required_keys if key not in bundle]
    if missing:
        fail(f"{source_path} 解析结果缺少必需字段: {', '.join(missing)}")
    components = bundle.get("components")
    if not isinstance(components, dict):
        fail("components 必须是对象")
    schemas = components.get("schemas")
    if not isinstance(schemas, dict):
        fail("components.schemas 必须是对象")
    paths = bundle.get("paths")
    if not isinstance(paths, dict):
        fail("paths 必须是对象")
    return bundle


def render_bundle(bundle: dict[object, object]) -> str:
    payload = yaml.dump(
        bundle,
        Dumper=CompactDumper,
        allow_unicode=True,
        sort_keys=False,
        width=120,
        default_flow_style=False,
    ).rstrip()
    return BUNDLE_HEADER + payload + "\n"


def main() -> int:
    args = parse_args()
    source_path = Path(args.source).resolve()
    output_path = Path(args.output).resolve()

    bundle = load_bundle(source_path)
    rendered = render_bundle(bundle)

    if args.check:
        if not output_path.is_file():
            fail(f"bundle 文件不存在: {output_path}")
        existing = output_path.read_text(encoding="utf-8")
        if existing != rendered:
            fail(
                f"OpenAPI bundle 未同步: {output_path}\n"
                "请运行: python3 scripts/sync_openapi_from_contract.py"
            )
        print(f"[openapi-sync] bundle 已同步: {output_path.relative_to(REPO_ROOT)}")
        return 0

    output_path.write_text(rendered, encoding="utf-8")
    print(
        "[openapi-sync] 已生成 "
        f"{output_path.relative_to(REPO_ROOT)} "
        f"(source: {source_path.relative_to(REPO_ROOT)})"
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
