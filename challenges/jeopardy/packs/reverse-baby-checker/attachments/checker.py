data = [
    0x1c, 0x16, 0x1b, 0x1d, 0x01, 0x08, 0x1f, 0x0c, 0x1f, 0x08, 0x09,
    0x1f, 0x25, 0x18, 0x1b, 0x18, 0x03, 0x25, 0x19, 0x12, 0x1f, 0x19,
    0x11, 0x1f, 0x08, 0x07,
]


def recover():
    return "".join(chr(x ^ 0x7A) for x in data)


def check(user_input):
    return user_input == recover()


if __name__ == "__main__":
    guess = input("flag> ").strip()
    print("correct" if check(guess) else "wrong")
