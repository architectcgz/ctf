CREATE TABLE public.awd_defense_workspaces (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    contest_id bigint NOT NULL,
    team_id bigint NOT NULL,
    service_id bigint NOT NULL,
    instance_id bigint NOT NULL,
    workspace_revision bigint NOT NULL DEFAULT 1,
    status character varying(24) NOT NULL DEFAULT 'pending',
    container_id character varying(64) NOT NULL DEFAULT ''::character varying,
    seed_signature text NOT NULL DEFAULT ''::text,
    created_at timestamp with time zone DEFAULT now() NOT NULL,
    updated_at timestamp with time zone DEFAULT now() NOT NULL,
    CONSTRAINT awd_defense_workspaces_contest_id_fkey FOREIGN KEY (contest_id) REFERENCES public.contests(id) ON DELETE CASCADE,
    CONSTRAINT awd_defense_workspaces_team_id_fkey FOREIGN KEY (team_id) REFERENCES public.teams(id) ON DELETE CASCADE,
    CONSTRAINT awd_defense_workspaces_service_id_fkey FOREIGN KEY (service_id) REFERENCES public.contest_awd_services(id) ON DELETE CASCADE,
    CONSTRAINT awd_defense_workspaces_instance_id_fkey FOREIGN KEY (instance_id) REFERENCES public.instances(id) ON DELETE CASCADE
);

CREATE UNIQUE INDEX uk_awd_defense_workspaces_scope
ON public.awd_defense_workspaces USING btree (contest_id, team_id, service_id);

CREATE INDEX idx_awd_defense_workspaces_instance_id
ON public.awd_defense_workspaces USING btree (instance_id);
