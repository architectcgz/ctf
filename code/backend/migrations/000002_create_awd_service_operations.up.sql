CREATE TABLE public.awd_service_operations (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    contest_id bigint NOT NULL,
    team_id bigint NOT NULL,
    service_id bigint NOT NULL,
    instance_id bigint NOT NULL,
    operation_type character varying(24) NOT NULL,
    requested_by character varying(16) NOT NULL,
    requested_by_id bigint,
    reason character varying(128) DEFAULT ''::character varying NOT NULL,
    sla_billable boolean DEFAULT true NOT NULL,
    status character varying(24) NOT NULL,
    error_message text DEFAULT ''::text NOT NULL,
    started_at timestamp with time zone NOT NULL,
    finished_at timestamp with time zone,
    created_at timestamp with time zone DEFAULT now() NOT NULL,
    updated_at timestamp with time zone DEFAULT now() NOT NULL,
    CONSTRAINT awd_service_operations_contest_id_fkey FOREIGN KEY (contest_id) REFERENCES public.contests(id) ON DELETE CASCADE,
    CONSTRAINT awd_service_operations_team_id_fkey FOREIGN KEY (team_id) REFERENCES public.teams(id) ON DELETE CASCADE,
    CONSTRAINT awd_service_operations_service_id_fkey FOREIGN KEY (service_id) REFERENCES public.contest_awd_services(id) ON DELETE CASCADE,
    CONSTRAINT awd_service_operations_instance_id_fkey FOREIGN KEY (instance_id) REFERENCES public.instances(id) ON DELETE CASCADE
);

CREATE INDEX idx_awd_service_operations_scope ON public.awd_service_operations USING btree (contest_id, team_id, service_id, id DESC);
CREATE INDEX idx_awd_service_operations_window ON public.awd_service_operations USING btree (started_at, finished_at);

CREATE UNIQUE INDEX uk_awd_service_operations_active
ON public.awd_service_operations USING btree (contest_id, team_id, service_id)
WHERE status IN ('requested', 'provisioning', 'recovering');
