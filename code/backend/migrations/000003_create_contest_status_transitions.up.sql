ALTER TABLE public.contests
ADD COLUMN status_version bigint DEFAULT 0 NOT NULL;

CREATE TABLE public.contest_status_transitions (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    contest_id bigint NOT NULL,
    status_version bigint NOT NULL,
    from_status character varying(20) NOT NULL,
    to_status character varying(20) NOT NULL,
    reason character varying(32) NOT NULL,
    applied_by character varying(64) NOT NULL,
    side_effect_status character varying(24) NOT NULL,
    side_effect_error text DEFAULT ''::text NOT NULL,
    occurred_at timestamp with time zone NOT NULL,
    created_at timestamp with time zone DEFAULT now() NOT NULL,
    updated_at timestamp with time zone DEFAULT now() NOT NULL,
    CONSTRAINT contest_status_transitions_contest_id_fkey FOREIGN KEY (contest_id) REFERENCES public.contests(id) ON DELETE CASCADE
);

CREATE UNIQUE INDEX uk_contest_status_transitions_contest_version
ON public.contest_status_transitions USING btree (contest_id, status_version);

CREATE INDEX idx_contest_status_transitions_occurred_at
ON public.contest_status_transitions USING btree (occurred_at DESC);
